package main

import (
	"bytes"
	"compress/flate"
	"context"
	"encoding/base64"
	"encoding/json"
	"fmt"
	"os"
	"path/filepath"
	"text/template"

	"github.com/getkin/kin-openapi/openapi3"
)

const embeddedSpecTemplate = `// Code generated by cmd/generate/specification; DO NOT EDIT.
package specification

import (
	"bytes"
	"compress/flate"
	"encoding/base64"
	"fmt"
	"strings"
)

// dataplaneSpec is the full HAProxy Data Plane API OpenAPI v3 specification,
// base64-encoded and compressed with deflate.
var dataplaneSpec = []string{
{{- range .Chunks}}
	"{{.}}",
{{- end}}
}

func decodeDataplaneSpec() ([]byte, error) {
	encoded := strings.Join(dataplaneSpec, "")
	compressed, err := base64.StdEncoding.DecodeString(encoded)
	if err != nil {
		return nil, fmt.Errorf("error base64 decoding spec: %w", err)
	}
	zr := flate.NewReader(bytes.NewReader(compressed))
	var buf bytes.Buffer
	if _, err := buf.ReadFrom(zr); err != nil {
		return nil, fmt.Errorf("read flate: %w", err)
	}
	if err := zr.Close(); err != nil {
		return nil, fmt.Errorf("close flate reader: %w", err)
	}
	return buf.Bytes(), nil
}

var rawDataplaneSpec = decodeDataplaneSpecCached()

func decodeDataplaneSpecCached() func() ([]byte, error) {
	data, err := decodeDataplaneSpec()
	return func() ([]byte, error) {
		return data, err
	}
}

// GetDataplaneSpecJSON returns the raw JSON bytes of the full embedded OpenAPI v3 specification.
// The result is cached at package init time, so repeated calls are cheap.
func GetDataplaneSpecJSON() ([]byte, error) {
	return rawDataplaneSpec()
}
`

// buildEmbeddedGoSpec reads the combined YAML spec from specDir/build/dataplane_spec.yaml,
// converts it to JSON, and writes an embedded Go source file to outputPath.
func buildEmbeddedGoSpec(specDir, outputPath string) error {
	specPath := filepath.Join(specDir, "build", "dataplane_spec.yaml")

	loader := openapi3.NewLoader()
	loader.IsExternalRefsAllowed = true
	doc, err := loader.LoadFromFile(specPath)
	if err != nil {
		return fmt.Errorf("loading %s: %w", specPath, err)
	}
	// Hard-fail on structural validation errors, but tolerate invalid examples:
	// the client-native spec currently ships examples that don't match their
	// schemas, and we need to keep working with it as-is.
	if err = doc.Validate(context.Background(), openapi3.DisableExamplesValidation()); err != nil {
		return fmt.Errorf("spec validation: %w", err)
	}
	if err = doc.Validate(context.Background()); err != nil {
		fmt.Fprintf(os.Stderr, "warning: spec example validation: %v\n", err)
	}

	jsonBytes, err := json.Marshal(doc)
	if err != nil {
		return fmt.Errorf("marshaling spec to JSON: %w", err)
	}

	chunks, err := compressAndChunk(jsonBytes)
	if err != nil {
		return fmt.Errorf("compressing spec: %w", err)
	}

	tmpl, err := template.New("embed").Parse(embeddedSpecTemplate)
	if err != nil {
		return fmt.Errorf("parsing template: %w", err)
	}

	var buf bytes.Buffer
	if err = tmpl.Execute(&buf, struct{ Chunks []string }{Chunks: chunks}); err != nil {
		return fmt.Errorf("executing template: %w", err)
	}

	if err = os.MkdirAll(filepath.Dir(outputPath), 0o755); err != nil {
		return fmt.Errorf("creating output dir: %w", err)
	}
	if err = os.WriteFile(outputPath, buf.Bytes(), 0o644); err != nil { //nolint:gosec // generated Go source is non-sensitive and meant to be world-readable
		return fmt.Errorf("writing %s: %w", outputPath, err)
	}
	fmt.Printf("wrote embedded spec: %s\n", outputPath)
	return nil
}

// compressAndChunk deflate-compresses data, base64-encodes it, and splits
// the result into 76-character chunks (matching oapi-codegen's output format).
func compressAndChunk(data []byte) ([]string, error) {
	var compressed bytes.Buffer
	w, err := flate.NewWriter(&compressed, flate.BestCompression)
	if err != nil {
		return nil, err
	}
	if _, err = w.Write(data); err != nil {
		return nil, err
	}
	if err = w.Close(); err != nil {
		return nil, err
	}

	encoded := base64.StdEncoding.EncodeToString(compressed.Bytes())

	const chunkSize = 76
	var chunks []string
	for len(encoded) > 0 {
		n := min(chunkSize, len(encoded))
		chunks = append(chunks, encoded[:n])
		encoded = encoded[n:]
	}
	return chunks, nil
}
